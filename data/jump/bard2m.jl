using JuMP
function build_model()
    model = Model()
    @variable(model, 0 <= x11 <= 10)
    @variable(model, 0 <= x12 <= 5)
    @variable(model, 0 <= x21 <= 15)
    @variable(model, 0 <= x22 <= 20)
    @variable(model, 0 <= y11 <= 20)
    @variable(model, 0 <= y12 <= 20)
    @variable(model, m_c11 <= 0)
    @variable(model, m_c12 <= 0)
    @variable(model, 0 <= y21 <= 40)
    @variable(model, 0 <= y22 <= 40)
    @variable(model, m_c21 <= 0)
    @variable(model, m_c22 <= 0)
    @constraint(model, side, x11 + x12 + x21 + x22 <= 40)
    @constraint(model, c11, - (0.4 * y11 + 0.7 * y12 - x11) ⟂ m_c11 <= 0)
    @constraint(model, c12, - (0.6 * y11 + 0.3 * y12 - x12) ⟂ m_c12 <= 0)
    @constraint(model, d_y11, 0 == 2 * (y11 - 4) - m_c11 * 0.4 - m_c12 * 0.6 ⟂ y11)
    @constraint(model, d_y12, 0 == 2 * (y12 - 13) - m_c11 * 0.7 - m_c12 * 0.3 ⟂ y12)
    @constraint(model, c21, - (0.4 * y21 + 0.7 * y22 - x21) ⟂ m_c21 <= 0)
    @constraint(model, c22, - (0.6 * y21 + 0.3 * y22 - x22) ⟂ m_c22 <= 0)
    @constraint(model, d_y21, 0 == 2 * (y21 - 35) - m_c21 * 0.4 - m_c22 * 0.6 ⟂ y21)
    @constraint(model, d_y22, 0 == 2 * (y22 - 2) - m_c21 * 0.7 - m_c22 * 0.3 ⟂ y22)
    @objective(model, Min, - (200 - y11 - y21) * (y11 + y21) - (160 - y12 - y22) * (y12 + y22))
    return model
end
